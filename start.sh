#!/bin/bash

# قراءة كلمة المرور من المتغير، وإذا لم توجد يضع كلمة مرور افتراضية
PASSWORD=${SSH_PASSWORD:-"RootPassword123!"}

# تعيين كلمة المرور للمستخدم root
echo "root:$PASSWORD" | chpasswd

echo "=============================================="
echo "SSH Server هو الآن جاهز للعمل!"
echo "=============================================="

# تشغيل سيرفر الـ SSH في الواجهة الأمامية لمنع الحاوية من التوقف
exec /usr/sbin/sshd -D

